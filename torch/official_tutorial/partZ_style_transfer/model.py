'''
We will add this content loss module directly after the
convolution layer(s) that are being used to compute the
content distance. This way each time the network is fed
an input image the content losses will be computed at
the desired layers and because of auto grad, all the
gradients will be computed. Now, in order to make the
content loss layer transparent we must define a forward
method that computes the content loss and then returns
the layerâ€™s input. The computed loss is saved as a
parameter of the module.
'''
class ContentLoss(nn.Module):
    def __init__(self, target,):
        super(ContentLoss, self).__init__()
        # we 'detach' the target content from the tree used
        # to dynamically compute the gradient: this is a stated value,
        # not a variable. Otherwise the forward method of the criterion
        # will throw an error.
        self.target = target.detach()

    def forward(self, input):
        self.loss = F.mse_loss(input, self.target)
        return input

